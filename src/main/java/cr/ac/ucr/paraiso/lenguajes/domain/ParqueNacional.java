package cr.ac.ucr.paraiso.lenguajes.domain;

public class ParqueNacional {
    private int idParqueNacional;
    private String nombre;

    public ParqueNacional() {
    }

    public int getIdParqueNacional() {
        return idParqueNacional;
    }

    public void setIdParqueNacional(int idParqueNacional) {
        this.idParqueNacional = idParqueNacional;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
